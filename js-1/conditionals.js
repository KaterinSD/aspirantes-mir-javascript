function calcularColor(color) {
    var color;
    if (color === 1) {
        console.log("negro");
    } else if (color === 2) {
        console.log("blanco");
    } else if (color === 3) {
        console.log("rojo");
    } else {
        console.log("verde");
    }

    return "El color es " + "";
};

console.log(calcularColor(1))
console.log(calcularColor(2))
